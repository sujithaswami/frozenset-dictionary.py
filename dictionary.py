#implicit

#syntax:variableName={key:value,key:value}

details={'firstName':'  Sujitha','lastName':'Swami','Email':'sujithaswami@gmail.com'}
print(details)
print(type(details))

#Explicit

#syntax:variableName=dict({key:value,key:value})

details=dict({'firstName':'Sujitha','lastName':'Swami','Email':'sujithaswami@gmail.com'})
print(details)
print(type(details))

#datatype/Variable Annotation

#syntax:variableName:dict={key:value,key:value}

details:dict={'firstName':'Sujitha','lastName':'Swami','Email':'sujithaswami@gmail.com'}
print(details)
print(type(details))